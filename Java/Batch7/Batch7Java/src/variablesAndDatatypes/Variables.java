package variablesAndDatatypes;

public class Variables {
	//Primitive data-type (fixed-size, always starts with lower case)
	byte a = -128;   //-128 to +127 (-2^8 to 2^8)
	short b = 12345;
	int c =  123456871;
	long d = 1234567893;
	float f = 100.21f;
	double g = 10001.123545;
	char h = 'A';
	boolean i = true;

	//Non-Primitive data-type (dynamic-size, always starts with upper case)
	String j = "Batch-07";
	Class k = Variables.class;	
}
